package com.example.superheroes;

public class PowerStats {
        int intelligence;
        int strength;
        int speed;
        int durability;
        int power;
        int combat;

        public int getIntelligence() {
                return intelligence;
        }

        public int getStrength() {
                return strength;
        }

        public int getSpeed() {
                return speed;
        }

        public int getDurability() {
                return durability;
        }

        public int getPower() {
                return power;
        }

        public int getCombat() {
                return combat;
        }
}
